

public class Card {
    private String color;
    private int value;
    private String action;

    public Card(String color, int value, String action) {
        this.color = color;
        this.value = value;
        this.action = action;
    }

    public String getColor() {
        return color;
    }

    public int getValue() {
        return value;
    }

    public String getAction() {
        return action;
    }
}





